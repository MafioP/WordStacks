
class Word {
	private final String word;
	private final int x;
	private final int y;
	
	public Word(String word, int x, int y) {
		this.word = word;
		this.x = x;
		this.y = y;
	}

	public String getWord() {
		return word;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	
}